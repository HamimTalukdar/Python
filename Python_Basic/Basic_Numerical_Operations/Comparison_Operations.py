# Equal to
result_eq = (5 == 5)
print("Equal to:", result_eq)

# Not equal to
result_neq = (10 != 5)
print("Not equal to:", result_neq)

# Greater than
result_gt = (8 > 3)
print("Greater than:", result_gt)

# Less than
result_lt = (4 < 7)
print("Less than:", result_lt)

# Greater than or equal to
result_gte = (10 >= 10)
print("Greater than or equal to:", result_gte)

# Less than or equal to
result_lte = (5 <= 3)
print("Less than or equal to:", result_lte)
