'''
& (Bitwise AND)
| (Bitwise OR)
^ (Bitwise XOR)
~ (Bitwise NOT)
<< (Left shift)
>> (Right shift)
'''

a = 5   # 101 in binary
b = 3   # 011 in binary
print(a & b)   # 1 (AND operation)
print(a | b)   # 7 (OR operation)
print(a ^ b)   # 6 (XOR operation)
print(~a)      # -6 (NOT operation)
print(a << 1)  # 10 (Left shift by 1)
print(a >> 1)  # 2  (Right shift by 1)
