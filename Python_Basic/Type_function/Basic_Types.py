'''In Python, the type() function is used to get the type of an object. It returns the type of the specified object, whether it's a built-in type or a user-defined class'''
# Integer
num = 42
print(type(num))  # <class 'int'>

# Floating-point number
float_num = 3.14
print(type(float_num))  # <class 'float'>

# String
text = "Hello, World!"
print(type(text))  # <class 'str'>

# Boolean
is_true = True
print(type(is_true))  # <class 'bool'>
