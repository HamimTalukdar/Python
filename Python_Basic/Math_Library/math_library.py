import math

# Basic arithmetic functions
print("Absolute value:", math.fabs(-5.6))
print("Ceiling (smallest integer greater than or equal to x):", math.ceil(4.3))
print("Floor (largest integer less than or equal to x):", math.floor(4.7))
print("Exponentiation (e raised to the power of x):", math.exp(2))
print("Logarithm base e:", math.log(8))
print("Logarithm base 10:", math.log10(100))
print("Power:", math.pow(2, 3))
print("Square root:", math.sqrt(25))

# Trigonometric functions (angles are in radians)
print("Sine:", math.sin(math.radians(30)))
print("Cosine:", math.cos(math.radians(45)))
print("Tangent:", math.tan(math.radians(60)))

# Angular conversion
print("Degrees to radians:", math.radians(180))
print("Radians to degrees:", math.degrees(math.pi))

# Constants
print("Value of pi:", math.pi)
print("Value of Euler's number (e):", math.e)
