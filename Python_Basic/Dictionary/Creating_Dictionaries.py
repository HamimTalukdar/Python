# Creating an empty dictionary
empty_dict = {}

# Creating a dictionary with key-value pairs
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}

# Accessing values
print(my_dict['name'])  # John
print(my_dict['age'])   # 25
print(my_dict['city'])  # New York

# Modifying values
my_dict['age'] = 26
print(my_dict['age'])   # 26

